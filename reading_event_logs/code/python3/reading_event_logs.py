print(cleanCmtdAbi)
# Load the above ABI as a json object
jsonData = json.loads(cleanCmtdAbi)
# Loop through the json object and find all of the events and their associated data
for abiComponents in jsonData:
	isEvent = False
	name = ""
	inputs = []
	for key, value in abiComponents.items():
		if key == "inputs":
			inputs = value
		if key == "name":
			name = value
		if key == "type":
			if value == "event":
				isEvent = True
	if isEvent is True and name is not "":
		print("\nEvent Log Name:" + name)
		print("Inputs:")
		inputCounter = 1
		for input in inputs:
			print("\t" + "Input" + str(inputCounter))
			for inputKey, inputValue in input.items():
				print("\t\t" + str(inputKey) + ":" + str(inputValue))
			inputCounter = inputCounter + 1
